<script lang="ts" setup>

interface OrderList {
  first: number
  second: number
  third: number
}

interface Props {
  id: number | null
  regist: OrderList | null
  place: string
  remark: string
  n: number | null
  setting: boolean | null
}

interface Emits {
  (e: 'reloadPlace', v: null): void
}

const place = withDefaults(defineProps<Props>(), {
  id: null,
  regist: null,
  place: '',
  remark: '',
  n: null,
  setting: null
})

const emits = defineEmits<Emits>()
const reloadPlace = () => {
  emits('reloadPlace', null)
}

const isEditPlace = ref<boolean>(false)
const openEditPlace = () => {
  isEditPlace.value = true
}
</script>

<template>
  <div class="text-[#333] font-light tracking-widest">
    <RegistInfoWideCard>
      <template #body>
        <div class="w-[15%] h-[90%] ml-8 text-3xl">
          第{{ place.n }}希望
        </div>
        <RegistInfoDivideBar />
        <div class="w-[40%] text-center text-4xl">
          {{ place.place }}
        </div>
        <div class="w-[7%] h-[80%] text-base">追記：</div>
          <div class="w-[30%] h-[80%] pr-1 break-normal">
            {{ place.remark }}
          </div>
      </template>
      <template #method>
        <div class="mx-4">
          <EditButton @click="openEditPlace()" />
        </div>
      </template>
    </RegistInfoWideCard>
  </div>
  <RegistInfoEditPlace
    v-if="isEditPlace"
    v-model:edit-place="isEditPlace"
    :id="place.id"
    :first="regist?.first"
    :second="regist?.second"
    :third="regist?.third"
    :remark="remark"
    @reload-place="reloadPlace()"
  />
</template>
